    public void segarkan(){
        model.getDataVector().removeAllElements();
        this.model.fireTableDataChanged();
        try {
            String sql ="SELECT * FROM rincian";
            Statement st = konek.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                Object as [] = new Object[9];
                as[0] = rs.getString("id_penjualan");
                as[1] = rs.getString("id_buku");
                as[2] = rs.getString("judul_buku");
                as[3] = rs.getString("noisbn");
                as[4] = rs.getString("penerbit");
                as[5] = rs.getString("tahun_terbit");
                as[6] = rs.getString("harga");
                as[7] = rs.getString("jumlah");
                as[8] = rs.getString("stok");
                model.addRow(as);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    public void kodeotopj(){
        try {
            String sql = "SELECT sum(kode) as iniv FROM kodeoto";
            Statement st = konek.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                int id_pjl = rs.getInt("iniv")+1;
                
                id_pj.setText("PJDJTTMR0"+String.valueOf(id_pjl));
            }
        } catch (Exception e) {
        }
    }
